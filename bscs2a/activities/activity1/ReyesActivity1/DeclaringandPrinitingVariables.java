package declaringandprinitingvariables;

public class DeclaringandPrinitingVariables {
    
    public static void main(String[] args) {
        int Number;
        char letter;
        boolean result;
        String word;
        
        Number = 10;
        letter = 'a';
        result = true;
        word = "hello";
        
        System.out.printf("Number = %d \n",
                Number);
        System.out.printf("letter = %c \n",
                letter);
        System.out.printf("result = %b \n",
                result);
        System.out.printf("str = %s \n",
                word);
        
        
    }
    
}
