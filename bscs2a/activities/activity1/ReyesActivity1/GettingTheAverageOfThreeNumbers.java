package gettingtheaverageofthreenumbers;

public class GettingTheAverageOfThreeNumbers {

    public static void main(String[] args) {
        
        int num1 = 10, num2 = 20, num3 = 45;
        
        System.out.print("number 1: " + num1 + "\n");
        System.out.print("number 2: " + num2 + "\n");
        System.out.print("number 3: " + num3 + "\n");
        System.out.print("Average is = " + (num1 + num2 + num3)/3 + "\n");
    }
    
}
