package declaringandprintingvariables;

public class DeclaringAndPrintingVariables {

    public static void main(String[] args) {
        int myNum = 10;
        System.out.println("Number = " + myNum);
        char myLetter = 'a';
        System.out.println("letter = " + myLetter);
        boolean myBool = true;
        System.out.println("result = " + myBool);
        String myText = "hello";
        System.out.println("str = " + myText);
    } 
}